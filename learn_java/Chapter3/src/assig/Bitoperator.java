package assig;

public class Bitoperator {

	public static void main(String[] args) {

		int num1 = 5;
		int num2 = 11;
		int result = num1 &num2;
		System.out.println(result);
		System.out.println(num1 |num2);
		
		int num3 = 15;
		System.out.println(num3<<2);
		System.out.println(num3>>1);
	
	}

}
