package reference;

public class Subject {

	String subName;
	int score;
	
	public  void setName(String name)
	{
		this.subName = name;
	}
	public String getName()
	{
		return this.subName;
	}
	public void setScore(int scr)
	{
		this.score = scr;
	}
	public int getScore()
	{
		return this.score;
	}
}
